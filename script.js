function ThemeDarkBlue() {
  var element = document.body;
  element.classList.remove("dark-blue");
  element.classList.remove("pink");
  element.classList.remove("pink-dark");
  element.classList.remove("purpure");
  element.classList.remove("dark-black");
  element.classList.add("dark-blue");
}

